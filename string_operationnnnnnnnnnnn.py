class StringOperations:  
    def concatenate(self, str1, str2):
        return str1 + str2

    def to_upper(self, string):
        return string.upper()

    def to_lower(self, string):
        return string.lower()

    def reverse_string(self, string):
        return string[::-1]

    def get_length(self, string):
        return len(string)
